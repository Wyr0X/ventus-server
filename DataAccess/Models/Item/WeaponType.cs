public enum WeaponType
{
    Sword,       // Espadas: equilibrio entre daño y velocidad
    Axe,         // Hachas: alto daño, baja velocidad
    Dagger,      // Dagas: muy rápidas, poco daño, ideal para críticos
    Mace,        // Mazas: daño intermedio, bonus contra enemigos blindados
    Bow,         // Arcos: ataque a distancia física, requiere flechas
    Crossbow,    // Ballestas: más lentas que los arcos pero más potentes
    Staff,       // Bastones: para magos, daño mágico, pueden consumir maná
    Wand,        // Varitas: similar a staff pero más rápidos, menos poder
    Spear,       // Lanzas: más rango cuerpo a cuerpo
    Claw,        // Garras: armas duales, muy rápidas, ideal para asesinos
    Fist,        // Puños: sin arma o con guantes mágicos, daño base cuerpo a cuerpo
    Throwing,    // Armas arrojadizas: dagas, estrellas, jabalinas
    Gun          // Armas de fuego si agregás steampunk o tecnología (opcional)
}
